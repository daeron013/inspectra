async function main() {
  const options = parseArgs(process.argv.slice(2));
  const outputDir = path.resolve(process.cwd(), options.outDir);
  await fs.mkdir(outputDir, { recursive: true });

  const documents = [
    supplierCertificate(options.orgName),
    incomingInspection(options.orgName),
    ncrReport(options.orgName),
    capaPlan(options.orgName),
    lotTraceability(options.orgName),
  ];

  for (const document of documents) {
    await writePdf(path.join(outputDir, document.fileName), document.build);
  }

  console.log(`Generated ${documents.length} sample PDF documents in ${outputDir}`);
}

main().catch((error) => {
  console.error("generate sample documents failed", error);
  process.exitCode = 1;
});
