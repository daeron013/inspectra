import fs from "fs/promises";
import path from "path";

import dotenv from "dotenv";
import { jsPDF } from "jspdf";

dotenv.config({ path: ".env.local" });
dotenv.config();

function parseArgs(argv) {
  const options = {
    outDir: "demo-documents",
    orgName: process.env.DEMO_ORG_NAME || "Acme Medical Devices",
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--out-dir" && argv[index + 1]) {
      options.outDir = argv[index + 1];
      index += 1;
    } else if (arg === "--org-name" && argv[index + 1]) {
      options.orgName = argv[index + 1];
      index += 1;
    }
  }

  return options;
}

function todayIso() {
  return new Date().toISOString().slice(0, 10);
}

function addHeader(doc, title, orgName, docId) {
  doc.setFillColor(140, 118, 91);
  doc.rect(0, 0, 210, 28, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(22);
  doc.text("Inspectra", 14, 18);
  doc.setFontSize(14);
  doc.text(title, 196, 12, { align: "right" });
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.text(orgName, 196, 19, { align: "right" });
  doc.text(`Doc ID: ${docId}`, 196, 24, { align: "right" });
  doc.setTextColor(43, 38, 34);
}

function addFooter(doc, pageNumber) {
  doc.setDrawColor(220, 214, 205);
  doc.line(14, 285, 196, 285);
  doc.setFontSize(9);
  doc.setTextColor(102, 95, 87);
  doc.text(`Generated by Inspectra Demo Kit • ${todayIso()} • Page ${pageNumber}`, 14, 290);
  doc.setTextColor(43, 38, 34);
}

function addSection(doc, heading, bodyLines, startY) {
  let y = startY;
  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.text(heading, 14, y);
  y += 7;
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);

  for (const line of bodyLines) {
    const rendered = doc.splitTextToSize(line, 178);
    doc.text(rendered, 14, y);
    y += rendered.length * 5 + 2;
  }

  return y + 2;
}

async function writePdf(filePath, build) {
  const doc = new jsPDF({ unit: "mm", format: "a4" });
  build(doc);
  const buffer = Buffer.from(doc.output("arraybuffer"));
  await fs.writeFile(filePath, buffer);
}

